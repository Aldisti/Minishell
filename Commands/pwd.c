#include "./../minishell.h"

int	print_pwd(t_shell *shell)
{
	char	*str;

	str = getcwd(0, 0);
	if (!str)
		ft_die(shell, 1, 12);
	printf("%s\n", str);
	free(str);
	return (0);
}
